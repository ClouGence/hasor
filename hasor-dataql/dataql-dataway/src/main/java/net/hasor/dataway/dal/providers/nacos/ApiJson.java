package net.hasor.dataway.dal.providers.nacos;
/**
 * 存储在 nacos 上的记录变更索引内容数据结构。
 * @author 赵永春 (zyc@hasor.net)
 * @version : 2020-09-21
 */
public class ApiJson {
    private String id;
    private String path;
    private long   time;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public long getTime() {
        return time;
    }

    public void setTime(long time) {
        this.time = time;
    }
}